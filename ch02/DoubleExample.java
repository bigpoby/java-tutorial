public class  DoubleExample {
    public static void main(String[] args) {
        double pi = 3.14159;
        double temperature = -12.5;
        double sum = pi + temperature;

        System.out.println("원주율: " + pi);
        System.out.println("기온: " + temperature);
        System.out.println("합계: " + sum);
    }
}